package utils

import (
	"crypto/rand"
	"fmt"
)

// Generate UUID

func GenerateUUID() string {
	// Generate UUID
	b := make([]byte, 16)
	_, err := rand.Read(b)
	if err != nil {
		fmt.Println("Error: ", err)
		return ""
	}

	uuid := fmt.Sprintf("%x-%x-%x-%x-%x", b[0:4], b[4:6], b[6:8], b[8:10], b[10:])

	return uuid
}
